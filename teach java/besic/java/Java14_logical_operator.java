package besic.java;

public class Java14_logical_operator {

    public static void main(String[] arry) {
        int x = 50;
        int y = 70;
        int z = 90;
        System.out.println((x >= 50) && (y >= 50) && (z >= 50));//Ouput true
        int exam = 40;
        int project = 80;
        System.out.println((exam >= 50) || (project >= 50));//Ouput true
        boolean pass = true;
        System.out.println(!pass);//Ouput false

    }

}
