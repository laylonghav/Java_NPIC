package besic.java;

public class Java13_comparison_operator {

    //Java13_comparison_operator
    public static void main(String[] arry) {

        int x = 10;
        int y = 12;
        System.out.println(x == y);//Ouput false
        System.out.println(x != y);//Ouput true
        System.out.println(x > y);//Ouput false
        System.out.println(x < y);//Ouput true
        System.out.println(x <= y);//Ouput true
        System.out.println(x >= y);//Ouput false

    }
}
