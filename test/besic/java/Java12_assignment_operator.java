package besic.java;

public class Java12_assignment_operator {
    //assignment operator

    public static void main(String[] arry) {
        int x = 10;

        System.out.println(x += 3);//x=x+3 // Ouput 13
        System.out.println(x -= 5);//x=x-5 // Ouput 8
        System.out.println(x *= 2);//x=x*2 // Ouput 16
        System.out.println(x /= 2);//x=/2 // Ouput 8
        System.out.println(x %= 3);//x=x%3 // Ouput 2
    }

}
